def adding_things(x,y):
	"""
	Takes two arguments,
	returns the sum of these two arguments.
	"""
	return x+y

def removing_things(z,v):
	"""
	Takes two arguments,
	subtracts second argument from the first one.
	"""
	return z-v

	
print(adding_things(2,2))
print(adding_things.__doc__)
print(removing_things(20, 5))
print(removing_things.__doc__)



